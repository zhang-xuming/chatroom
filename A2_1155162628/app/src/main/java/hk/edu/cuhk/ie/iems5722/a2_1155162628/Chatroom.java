package hk.edu.cuhk.ie.iems5722.a2_1155162628;

public class Chatroom {
    private String name;
    private int id;

    public Chatroom(String name, int id){
        this.name = name;
        this.id = id;
    }

    public String getName(){
        return name;
    }

    public int getId(){
        return id;
    }
}


